print('Integer casting')
number = 20
cast_number = str(number)
print(type(cast_number))

print('Float casting')
python_float = 20.5
cast_float = int(python_float)
print(cast_float)
print(type(cast_float))

print('String casting')
python_string = '500'
cast_string = int(python_string)
print(cast_string)
print(type(cast_string))
